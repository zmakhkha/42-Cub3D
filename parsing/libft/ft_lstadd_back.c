#include"libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*lastnode;

	if (!lst || !new)
		return ;
	if (*lst == NULL)
		*lst = new;
	else
	{
		lastnode = ft_lstlast(*lst);
		lastnode -> next = new;
	}
}
